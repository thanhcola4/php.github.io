                <p><strong>Terraria</strong> là một trò chơi thế giới mở 2D (2D sandbox) với lối chơi xoay quanh việc khám phá, xây dựng, chế tạo, chiến đấu và khai thác. Trò chơi có phong cách đồ họa dựa trên khối 3D sprite liên tưởng đến các họa tiết 16 bit được biết đến trên Super NES. Trò chơi được chú ý bởi phong cách chơi phiêu lưu khám phá cổ điển, tương tự như các trò chơi như sê-ri Metroid và Minecraft.
                    Trò chơi bắt đầu bằng việc tự tạo thế giới ngẫu nhiên (procedurally generated). Người chơi bắt đầu với ba công cụ cơ bản: một cái cuốc để đào, một thanh kiếm để chiến đấu và một cái rìu để chặt cây. Đa dạng tài nguyên, đáng chú ý là quặng có thể được tìm thấy trong khi khám phá hang động. Người chơi khi bắt đầu có số máu (health) giới hạn và mana (mana points) giới hạn, mà cả hai thứ đó có thể tăng lên bằng cách tìm kiếm vật phẩm (items). Một số tài nguyên và hầu hết các vật phẩm, có thể chỉ có thể được tìm thấy ở một số khu vực nhất định trên bản đồ, được giấu trong các rương thường và hiếm hoặc rơi ra từ một số quái. Người chơi sử dụng tài nguyên để chế tạo các vật phẩm và trang bị mới tại một trạm chế tạo phù hợp cho công thức đó. Ví dụ, bàn hoặc các vật phẩm khác có thể được chế tạo tại băng ghế làm việc (work bench), các thanh có thể được luyện từ quặng tại lò và áo giáp có thể được chế tạo tại một cái đe (anvil). Một số vật phẩm cao cấp ở Terraria yêu cầu nhiều thao tác chế tạo trong đó sản phẩm của một công thức được sử dụng làm nguyên liệu cho một công thức khác.
                </p>
